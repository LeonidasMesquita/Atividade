const numero = prompt("Digite um número inteiro:"); // solicita que o usuário digite um número inteiro
if (numero % 2 === 0) { // verifica se o número é divisível por 2, ou seja, se é par
  console.log(numero + " é par."); // imprime no console que o número é par
} else {
  console.log(numero + " é ímpar."); // caso contrário, imprime no console que o número é ímpar
}
