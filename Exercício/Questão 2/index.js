const numero1 = Number(prompt("Digite o primeiro número:")); // solicita o primeiro número ao usuário
const numero2 = Number(prompt("Digite o segundo número:")); // solicita o segundo número ao usuário

const soma = numero1 + numero2; // calcula a soma dos números
const subtracao = numero1 - numero2; // calcula a subtração dos números
const multiplicacao = numero1 * numero2; // calcula a multiplicação dos números
const divisao = numero1 / numero2; // calcula a divisão dos números

console.log("A soma é: " + soma); // imprime a soma no console
console.log("A subtração é: " + subtracao); // imprime a subtração no console
console.log("A multiplicação é: " + multiplicacao); // imprime a multiplicação no console
console.log("A divisão é: " + divisao); // imprime a divisão no console
